def encodeString(stringVal):
    prevChar =None 
    n = 0
    numLst =[1]
    alphLst = ["a"]
    for char in stringVal:
        if char != prevChar:
            numLst.append(n)
            alphLst.append(prevChar)
            n=0
            prevChar=char
        n+=1
        
    return numLst,alphLst

